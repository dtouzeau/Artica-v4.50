	if($GLOBALS["OUTPUT"]){echo "Configure.....: ".date("H:i:s")." [INIT]: {$GLOBALS["TITLENAME"]} /usr/bandwidthd/etc/bandwidthd.conf done\n";}
}

function PID_NUM(){
	$unix=new unix();
	$pid=$unix->get_pid_from_file("/var/run/bandwidthd.pid");
	if($unix->process_exists($pid)){return $pid;}
	return $unix->PIDOF("/usr/bandwidthd/bandwidthd");
}
